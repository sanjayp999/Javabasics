package com.assignmentbasics;

public class Andoroperators {
	public static void main(String[] args) {
		int a=20;
		int b=40;
		//AND operater
		if(a<b && b>a)
		System.out.println("true");
		else
			System.out.println("false");
		//OR operator
		if(a==b || b<a)
			System.out.println("true");
			else
				System.out.println("false");
		//Assigned a value
		System.out.println(a);
			
	}

}
